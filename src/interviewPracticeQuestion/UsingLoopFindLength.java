package interviewPracticeQuestion;

public class UsingLoopFindLength {
	public void findLength() {
		String Name="Hamneet";
		int findLength=0;
		System.out.println("the length of the String using loop"+Name.length());
		for(int i=0;i<Name.length();i++) {
			findLength++;
			
		}
		System.out.println("the length of the string is using loop:"+findLength);
	}

}
